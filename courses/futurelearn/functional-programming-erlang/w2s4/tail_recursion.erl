-module(tail_recursion).

-export([fac/1, fib/1]).

fac(N) ->
    fac(N, 1).

fac(0, Acc) ->
    Acc;
fac(N, Acc) when N > 0 ->
    fac(N - 1, Acc * N).

fib(N) ->
    fib(N, 0, 1).

fib(0, Acc1, _) ->
    Acc1;
fib(N, Acc1, Acc2) when N > 0 ->
    fib(N - 1, Acc2, Acc1 + Acc2).
    